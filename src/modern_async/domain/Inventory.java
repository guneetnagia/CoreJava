package modern_async.domain;

public class Inventory {
	private int count;
	
	

	public Inventory() {
		super();
	}

	public Inventory(int count) {
		super();
		this.count = count;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
}
