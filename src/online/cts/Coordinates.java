package online.cts;

public final class Coordinates {
	private Integer x;
	private Integer y;
	
	
	public Integer getX() {
		return x;
	}



	public Integer getY() {
		return y;
	}


	public void setX(Integer x){
		this.x = x;
	}



	public Coordinates(int x, int y){
		this.x = x;
		this.y = y;
	}
	
}
